export function removeAccents(text: string): string {
	const accentMap: { [key: string]: string } = {
		à: "a",
		á: "a",
		â: "a",
		ã: "a",
		ä: "a",
		å: "a",
		è: "e",
		é: "e",
		ê: "e",
		ë: "e",
		ì: "i",
		í: "i",
		î: "i",
		ï: "i",
		ò: "o",
		ó: "o",
		ô: "o",
		õ: "o",
		ö: "o",
		ù: "u",
		ú: "u",
		û: "u",
		ü: "u",
		ý: "y",
		ÿ: "y",
		ç: "c",
		ñ: "n",
		À: "A",
		Á: "A",
		Â: "A",
		Ã: "A",
		Ä: "A",
		Å: "A",
		È: "E",
		É: "E",
		Ê: "E",
		Ë: "E",
		Ì: "I",
		Í: "I",
		Î: "I",
		Ï: "I",
		Ò: "O",
		Ó: "O",
		Ô: "O",
		Õ: "O",
		Ö: "O",
		Ù: "U",
		Ú: "U",
		Û: "U",
		Ü: "U",
		Ý: "Y",
		Ÿ: "Y",
		Ç: "C",
		Ñ: "N",
	};

	return text
		.split("")
		.map((char) => accentMap[char] || char)
		.join("");
}
